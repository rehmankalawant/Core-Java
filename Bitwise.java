package packageCoreJava;


public class Bitwise {

	public static void main (String [] Args ) {
		
		int a = 44 , b = 07 ;
		
		System.out.println("AND " + (a & b) );
		System.out.println("OR " + (a | b) );
		System.out.println("XOR " + (a ^ b) );
		System.out.println("NOT " + (~ a));
	}
}
