package packageCoreJava;


public class AssignmentOPT {

	public static void main ( String [] Args ) {
		
     int a ;
     int b ;
     int c ;
     
     a = 44 ; // simple assignment operator 
     b = 07 ; // simple assignment operator
     c = 82 ; // simple assignment operator
     
     System.out.println(a);  // compound assignment operator
     
     b+=3;  // (b=b+3)
     System.out.println(b);  // compound assignment operator
     
     c-=2;  // (c=c-2)
     System.out.println(c);  // compound assignment operator
     
	}
}
